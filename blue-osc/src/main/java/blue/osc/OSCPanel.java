    void load() {
        
        final Preferences prefs = NbPreferences.forModule(
                    OSCManager.class);
        
        serverPortSpinner.setValue(prefs.getInt("serverPort", 8000));
    }

    void store() {
        
        final Preferences prefs = NbPreferences.forModule(
                    OSCManager.class);
        prefs.putInt("serverPort", ((SpinnerNumberModel)serverPortSpinner.getModel()).getNumber().intValue());
        
         try {
            prefs.sync();
        } catch (BackingStoreException ex) {
            Exceptions.printStackTrace(ex);
        }
    }

