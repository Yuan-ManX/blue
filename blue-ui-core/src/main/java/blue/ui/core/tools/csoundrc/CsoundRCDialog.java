    private void openCsoundRC() {
        String csoundRc = System.getenv("CSOUNDRC");
        
        
        String text = "";
        
        if(csoundRc != null) {
            csrcFile = new File(csoundRc);
            
        } else {
            String home = System.getenv("HOME");
            csrcFile = new File(home + File.separator + ".csound6rc");
        }
        
        if (csrcFile.exists()) {
            try {
                text = TextUtilities.getTextFromFile(csrcFile);
            } catch (FileNotFoundException ex) {
                Exceptions.printStackTrace(ex);
            } catch (IOException ex) {
                Exceptions.printStackTrace(ex);
            }
        }
        
        jTextArea1.setText(text);
        jTextArea1.setCaretPosition(0);
        fileLabel.setText(csrcFile.getAbsolutePath());
    }
